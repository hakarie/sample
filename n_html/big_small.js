function big(elt)
{	
	elt.style.fontSize = "20px";
	return;
}
function small(elt)
{
	elt.style.fontSize = "";
	return;
}
function imgbig(elt)
{
	elt.setAttribute("width", "400px");
	elt.setAttribute("height", "280px");
	return;
}
function imgsmall(elt)
{
	elt.setAttribute("width", "300px");
	elt.setAttribute("height", "180px");
	return;
}